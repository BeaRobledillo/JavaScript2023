//El método startsWith() indica si una cadena de texto comienza con los caracteres de una cadena de texto concreta, 
//devolviendo true o false según corresponda.

let myString = 'All Right!';

console.log(myString.startsWith('All')); // true

console.log(myString.startsWith('Todo')); // false




