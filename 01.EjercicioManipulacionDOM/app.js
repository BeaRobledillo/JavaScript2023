/**1. Definiremos una página web sencilla, que se compondrá de un contenedor (cuadro) situado en el centro de la página, este cambiará de color de manera dinámica para ello, se dará la libertad de desarrollar un sistema por el cual el usuario vea un listado del colores que pueda elegir para aplicarlo posteriormente al contenedor.
Añadiremos también una funcionalidad que permita ocultar y mostrar el cuadro. */
const btn = document.getElementById('btn')
const color = document.querySelector('.color')

btn.addEventListener('click', function () {
  const randomNumber = getRandomNumber()
  document.body.style.backgroundColor = colors[randomNumber]
  color.textContent = colors[randomNumber]
})

function getRandomNumber () {
  return Math.floor(Math.random() * colors.length)
}

